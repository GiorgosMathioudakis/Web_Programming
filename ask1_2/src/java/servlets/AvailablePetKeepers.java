    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String type = request.getParameter("type");
        System.out.println("In the servlet " + type);
        try {
            EditPetKeepersTable keeper = new EditPetKeepersTable();
            response.setStatus(200);

            ArrayList<PetKeeper> keepers = keeper.getPetKeepersTypeAndBookings(type);

            // Convert the list to JSON
            String json = new Gson().toJson(keepers);

            // Set the response content type to JSON
            response.setContentType("application/json");
            response.setCharacterEncoding("UTF-8");

            // Write the JSON string to the response
            response.getWriter().write(json);

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Registers.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

